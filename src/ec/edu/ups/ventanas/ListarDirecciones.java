    public void llenarDatos(){
        modelo = (DefaultTableModel) tabladir.getModel();
        Set<Direccion> lista = controladorDireccion.Listar();
        for (Direccion d : lista) {
            Object[] datos = {d.getCodigo(),
            d.getCallepreincipal(),
            d.getCalleSecundaria(),
            d.getNumero(),
            d.getPersonaCedula()};
            modelo.addRow(datos);
        }
    }

